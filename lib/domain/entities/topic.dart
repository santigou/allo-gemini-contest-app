class Topic {
  String id;
  String name;
  String description;
  String objectives;
  String languageId;
  String summary;
  int subtopicCount;

  Topic({
    required this.id,
    required this.name,
    required this.description,
    required this.objectives,
    required this.languageId,
    required this.summary,
    required this.subtopicCount,
  });
}
